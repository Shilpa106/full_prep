# Program to Encrypt a String using ! and @

# Given a string, the task is to encrypt this string using ! and @ symbols, alternatively. While encrypting the message the encrypted format must repeat
#  the symbol as many times as the letter position in Alphabetical order.
# Examples: 
 

# Input: string = "Ab" 
# Output: !@@
# Explanation:
# Position of 'A' in alphabetical order is 1 and in String is odd position so encrypted message will have 1 '!'

# Position of 'b' in alphabetical order is 2 and in String is even position so encrypted message will have 2 '@'

# Therefore, the output "!@@"

# Input: string = "CDE"
# Output: !!!@@@@!!!!!